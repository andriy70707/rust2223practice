#[test]
fn main() {
    let x: i32 = 5;
    {
        let x = 12;
        assert_eq!(x, 12);
    }

    assert_eq!(x, 5);

    let x = 42;
    println!("{}", x);
}


//Код на Rust демонструє області видимості змінних: x спочатку має значення 5, в вкладеній області змінюється на 12, але після виходу з неї знову стає 5.
// Потім x присвоюється значення 42, яке виводиться на екран.
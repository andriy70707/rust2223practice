// спочатку x отримує значення 3 з кортежу (3, 4), а потім y отримує значення 2 з масиву [1, 2].
// Далі макрос assert_eq! перевіряє, що значення [x, y] дорівнюють [3, 2].
// Якщо перевірка проходить, виводиться "Success!".
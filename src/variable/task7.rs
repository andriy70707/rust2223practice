#[test]
fn main() {
    let x = 1;

    println!("Значення x: {}", x);  // Перше використання

    let y = x + 1;                  // Друге використання
    println!("x + 1 дорівнює: {}", y);
}



//У цьому коді змінна x ініціалізується зі значенням 1, і це значення використовується двічі:
// спочатку для виведення значення x, а потім для обчислення нового значення y, яке дорівнює x + 1,
// після чого результат обчислення також виводиться на екран.